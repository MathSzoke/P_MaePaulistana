CREATE OR ALTER TRIGGER UtrCommonPacienteInsertUpdateCorrigirPacienteNomeMae
ON Common.Paciente
FOR	INSERT, UPDATE
AS
	IF EXISTS(SELECT * FROM Inserted WHERE Inserted.PacienteIdMae IS NOT NULL)
		UPDATE P 
		SET P.PacienteNomeMae = M.PacienteNome
		FROM Inserted I
		JOIN Common.Paciente P ON P.PacienteId = I.PacienteId
		JOIN Common.Paciente M ON M.PacienteId = P.PacienteIdMae
		WHERE I.PacienteIdMae IS NOT NULL
GO

CREATE OR ALTER TRIGGER UtrCommonEstabelecimentoInsertUpdateCorrigirCidadeId
ON Common.Estabelecimento
FOR	INSERT, UPDATE
AS
	IF EXISTS(SELECT * FROM Inserted WHERE SupervisaoTecnicaId IS NOT NULL)
		UPDATE E 
		SET E.CidadeId = C.CidadeId
		FROM Inserted I
		JOIN Common.Estabelecimento E ON E.EstabelecimentoId = I.EstabelecimentoId
		JOIN Common.SupervisaoTecnica ST ON ST.SupervisaoTecnicaId = E.SupervisaoTecnicaId
		JOIN Common.Coordenadoria C ON C.CoordenadoriaId = ST.CoordenadoriaId
		WHERE I.SupervisaoTecnicaId IS NOT NULL
GO

